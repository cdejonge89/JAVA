public class Variables {
    public static void main(String[] args) {
        int ourInt; // we can declarea variable without setting its value
        ourInt = 400; // we can assign a value to the variable later in our code
        double pi = 3.14159; // we can also declare and assign on the same line
        boolean bool = true;
        char singleCharacter = 'A';
        String multipleChars = "ABC";

        System.out.println(ourInt);
        System.out.println(pi);
        System.out.println(bool);
        System.out.println(singleCharacter);
        System.out.println(multipleChars);
    }
}